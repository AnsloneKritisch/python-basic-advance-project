# let see random generation of  data 

# we will use the random module in Python. This module provides functions for generating random numbers.
import random 

# generate a random integer between 1 and 10
random_number = random.randint(1,10)

print(random_number)  # output: a random integer between 1 and 10

# generate a random float between 0 and 1

random_float = random.random()

print(random_float)  # output: a random float between 0 and 1

# generate a random number 

random_float = random.uniform(1,10)
print(random_float) # output: a random float between 1 and 10

