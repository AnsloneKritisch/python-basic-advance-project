
# Example of a while loop in Python
# This loop will print numbers from 1 to 5

counter = 1  # Initialize the counter variable

# The while loop will keep running as long as the condition (counter <= 5) is True
while counter <= 5:
	print("Current value of counter:", counter)  # Print the current value
	counter += 1  # Increment the counter by 1 in each iteration

# When the condition becomes False (counter > 5), the loop stops
print("Loop finished!")
