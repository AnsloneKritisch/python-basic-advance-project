# Blackjack Game Project
# The game of Blackjack (also known as 21) is a comparing card game between one or more players and a dealer, where each player in turn competes against the dealer. Players do not compete against each other. The rules are as follows:

# The deck is unlimited in size. 
# There are no jokers. 
# The Jack/Queen/King all count as 10. 
# The Ace can count as 11 or 1, whichever is more advantageous to the player. 
# The player starts with two cards, one of the dealer's cards is hidden until the end of the game. 
# The player can request additional cards to be dealt, until they decide to stop. 
# Face cards (Jack, Queen, King) count at 10.
# If the player's hand exceeds 21 the player busts and the dealer wins.
# If the dealer's hand exceeds 21 the dealer busts and the player wins.
# If both player and dealer bust the winner is decided by comparing cards.
# The game ends when a player or the dealer decides to stop.

 